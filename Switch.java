class Switch
{
	public static void main(String args[])
			{
					for(int i=1;i<=10;i++)
						{
					switch(5)
							{
					case 1:
						System.out.println("january");
						break;
					case 2:
						System.out.println("fbruary");
						break;
					case 3:
						System.out.println("march");
						break;
					case 4:
						System.out.println("April");
						break;
					case 5:
						System.out.println("May");
						break;
					default:
						System.out.println("no matches");
						      }
						}
			}
}