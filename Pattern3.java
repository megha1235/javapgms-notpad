class Pattern3
{
public static void main(String args[]) 
    { 
        int i, j; 
  
        for(i=1; i<6; i++) 
        { 
  
            for(j=i; j<=i; j++) 
            { 
                
                System.out.print(i); 
				System.out.println(i+1); 
				
            } 
            
            System.out.println(" ");
	
        } 
   } 
  
   
} 
